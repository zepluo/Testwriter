    public void load()
    {
        
        A.setSelected(true);
        Easy.setSelected(true);
        questionNumberLabel.setText("Question # "+(frame.numQuestion+1));
          
        Questions cur = frame.questionList.get(frame.numQuestion);
        choiceA.setText(cur.getChoices()[0]);
        choiceB.setText(cur.getChoices()[1]);
        choiceC.setText(cur.getChoices()[2]);
        choiceD.setText(cur.getChoices()[3]);
        stemArea.setText(cur.getStem());
        fileLabel.setText(cur.getImageFile());
        String answer =cur.getCorrectAnswer();
        
            if(answer.equalsIgnoreCase("A"))
            {
                A.setSelected(true);
                
            }
            
            if(answer.equalsIgnoreCase("B"))
            {
                B.setSelected(true);
                
            }
            
            if(answer.equalsIgnoreCase("C"))
            {
                C.setSelected(true);
                
            }
            
            if(answer.equalsIgnoreCase("D"))
            {
                D.setSelected(true);
                
            }
            
            
        
        
        if(cur.getDifficulty()==0)
        {
            Easy.setSelected(true);
            
        }
        if(cur.getDifficulty()==1)
        {
            Medium.setSelected(true);
        }
        if(cur.getDifficulty()==2)
        {
            Hard.setSelected(true);
        }
        
      //image Loading
      imageLoad();
     
        
    }
    
    private void imageLoad()
    {
        Questions cur = frame.questionList.get(frame.numQuestion);
         ImageIcon image = new ImageIcon(new ImageIcon(cur.getImageFile()).getImage().getScaledInstance(200, 200, 0));
        System.out.println("MCPANEl loading imagefile...."+cur.getImageFile());
        JLabel imageL = new JLabel(image);
        
        imagePanel.setLayout(new BorderLayout());
            imagePanel.updateUI();
           imagePanel.removeAll();
        imagePanel.add(imageL,BorderLayout.CENTER);
        
    }
